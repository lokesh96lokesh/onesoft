package oops11;

public class Battery {
	private String brand;
	private int price;
	private int mAh;
	
	public void setBrand(String brand) {
		this.brand=brand;
	}
	public String getBrand() {
		return brand;
	}
	public void setPrice(int price) {
		this.price=price;
	}
	public int getPrice() {
		return price;
	}
	public void setMah(int mAh) {
		this.mAh=mAh;
	}
	public int getmAh() {
		return mAh;
	}

}
