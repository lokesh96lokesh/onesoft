package oops2;

public class Addition {
	public String add(int num1,int num2) {
		return  "Add values is " +(num1+num2);
	}
	public String add(int num1,float num2) {
		return "Add values is " +(num1+num2);
	}
	public String add(float num1,float num2) {
		return  "Add values is " +(num1+num2);
	}
	public String add(int num1,int num2,int num3) {
		return "Add values is "+(num1+num2+num3);
	}
	public String add(float num1,float num2,float num3) {
		return "Add values is "+(num1+num2+num3);
	}
	

}
