package day11;

public class Battery {
	private String brand;
	private int price;
	private int mah;
	public void setBrand(String brand) {
		this.brand=brand;
	}
	public String getBrand() {
		return brand;
	}
	public void setPrice(int price) {
		this.price=price;
	}
	public int getPrice() {
		return price;
	}
	public void setMah(int mah) {
		this.mah=mah;
	}
	public int getMah() {
		return mah;
	}

}
