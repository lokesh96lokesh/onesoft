package day1;

public class relationaloperators {
	public static void main (String[]args) {
		int number1 =46;
		int number2 =21;
		boolean greaterthen = number1<number2;
		boolean lesserthen = number1>number2;
		boolean equalto = number1==number2;
		System.out.println(lesserthen);
		System.out.println(greaterthen);
		System.out.println(equalto);
	}
}
